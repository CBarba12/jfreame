public class Tipo_de_Instrumentos {
    private String codigo;
    private String unidad;
    private String nombre;

    public Tipo_de_Instrumentos(String codigo, String unidad, String nombre) {
        this.codigo = codigo;
        this.unidad = unidad;
        this.nombre = nombre;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getUnidad() {
        return unidad;
    }

    public void setUnidad(String unidad) {
        this.unidad = unidad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
    
    
}
